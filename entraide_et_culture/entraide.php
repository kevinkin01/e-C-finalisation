                <h2>Vestiaire social</h2>
                <div class="paraEntraide">
                    <p><br>
                        L’association dispose d’un vestiaire social qui propose une large gamme de vêtements, d’accessoires, de chaussures, de linges de maison… pour toute la famille, tous les besoins et tous les goûts, en bon état. Il est ouvert à tous mais vise essentiellement à répondre aux besoins des personnes les plus en difficulté afin qu’elles puissent se vêtir à petit prix voire gratuitement.
                        <br>
                        <br>
                        Le vestiaire social est ouvert les mardis de 9h00 à 12h30 et les vendredis de 14h00 à 15h30. Il est toujours possible de prendre rendez-vous en dehors des horaires d’ouverture.
                        <br>
                        <br>
                        Plusieurs fois par an, notre équipe du vestiaire social organise des bourses aux vêtements qui se déclinent selon diverses thématiques tels que les quatre saisons, vêtements femmes, vêtements hommes, vêtements enfants/bébés, la rentrée scolaire, les vacances d’été…
                        <br>
                        <br>
                        Sous certaines conditions, le service social compose des colis vestimentaires gratuits à destination du public le plus précarisé qui ne dispose pas de moyens pour se vêtir, même à petits prix.
                        <br>
                        <br>
                        Nos stocks de vêtements, tout comme les réserves de denrées alimentaires, dépendent fortement de la générosité des particuliers qui, par leurs dons, nous permettent d’offrir des colis vestimentaires à un nombre important et croissant de personnes touchées par la crise économique. Les dons de vêtements peuvent être déposés à l’ASBL du lundi au vendredi entre 9h et 16h. Par respect pour notre personnel et pour le public qui en bénéficie, nous demandons de déposer uniquement des vêtements en bon état.
