                <h2>n.t.c.i</h2>
                <div class="paraCulture">
                    <p><br>
                        Depuis 2014, Entraide et Culture propose un soutien numérique à son public sous la forme d’initiations et de formations en informatique et aux technologies de l’information de la communication (TIC).
                        <br>
                        Nous accueillons principalement un public de quartier, fragilisé au niveau de l’emploi, avec de faibles revenus, disposant de peu de matériel et de peu de ressources en informatique.
                        <br>
                        Pour les aider au mieux, nos formations et initiations sont organisées par petits groupes répartis selon les besoins et les niveaux de chacun.
                        <br><br>
                        Les objectifs poursuivis sont les suivants :
                        <br>
                        - approche et présentation du matériel informatique ;
                        <br>
                        - apprentissage des bases en traitement de texte, tableur et présentation diaporama (type Power Point) ;
                        <br>
                        - initiation à Internet : création et usage courant d’une adresse mail, utilisation d’un moteur de recherche … ;
                        <br>
                        - découverte d'outils pratiques en ligne, comment consulter son compte en banque… ;
                        <br>
                        - découverte des périphériques et des différentes plateformes informatiques ;
                        <br>
                        - développement d’une réflexion préventive : obsolescence programmée, surconsommation, impact écologique et géopolitique.
                        <br>
                        L'apprentissage est conçu de manière à favoriser l’autonomie des apprenants face aux différentes interfaces en changement perpétuel et à les amener à développer une pensée construite et critique du monde numérique.
                        <br>
                        Aussi, le service propose plusieurs formations ponctuelles aux adultes sous forme de demi-journées et selon les demandes formulées telles que : comment protéger et nettoyer son ordinateur ?, comment se servir d’un Smartphone et d’une tablette numérique ?, comment stocker ces photos sur un espace virtuel ?...
                        <br><br>
                        Pour certains groupes, constitués à partir d’apprenants qui suivent aussi des cours d’alpha/Fle au sein de l’asbl, il s’agit de concilier un travail sur l’apprentissage de la langue à celui sur les notions d’informatique en s’intéressant précisément à l’étude du vocabulaire propre à l’informatique.
                        <br>
                        Le service TIC propose également des formations informatiques ou des remises à niveau pour les membres du personnel de l’asbl ainsi qu’une animation hebdomadaire avec les enfants les plus âgés de l’école de devoirs.
                        <br><br>
                        Afin d’aider notre public cible dans ses démarches administratives, nous proposons un Espace Public Numérique (EPN), ouvert tous les jours de la semaine avec accès à internet et à une imprimante. Par soucis organisationnels, cet espace est prioritairement réservé aux apprenants et bénéficiaires de l’asbl.
                        <br><br>
                        Aussi, différents partenariats ont pu être scellés au fil des années et des rencontres.
                        <br>
                        Enfin, l’ASBL remercie les différents donateurs qui ont permis au service de disposer de l’essentiel de son matériel informatique et de mieux équiper les services et les nouveaux apprenants. Grâce à eux, une bourse de matériel informatique à destination des apprenants a vu le jour.
                        <br><br>
                        Plus d’informations : Mme Emilie MAIDON au 02 539 43 60 ou tic@entraideetculture.be
                    </p>
