                <h2>école des devoirs</h2>
                <div class="paraCulture">
                    <p><br>
                        Entraide et Culture propose une école de devoirs (EdD) aux enfants âgés de 6 à 12 ans, essentiellement des élèves du primaire qui vivent dans les quartiers avoisinants. L’EdD accueille des enfants les lundis, mardis et jeudis de 15h30 à 17h30. Des activités culturelles et créatives sont régulièrement organisées le mercredi 13h à 17h et ponctuellement lors des congés scolaires.
                        <br><br>
                        Généralement, l’activité journalière de l’école de devoirs s’articule en trois parties :
                        <br>
                        - Un premier temps est destiné à l’accueil des enfants. Ce temps d’accueil est ponctué par un goûter à base de fruits, céréales et produits laitiers. Le goûter a lieu tous les jours d’ouverture de l’école de devoirs. Les enfants participent activement à ce goûter : préparation, vaisselle et remise en ordre des locaux...
                        <br>
                        - Un deuxième temps est consacré à l’aide aux devoirs à proprement parlé. Cette période ne peut idéalement excéder 45 minutes en harmonie avec le décret « devoir » voté en 2001. L’accompagnement aux devoirs est effectué en petits groupes, par niveaux ou individuellement si nécessaire.
                        <br>
                        - Un troisième temps privilégie les activités créatives. Elles sont conçues sur base des méthodes d’éducation active, mobilisant les bases élémentaires de tout apprentissage. Les enfants ont accès à la bibliothèque, à des jeux de société, à des instruments de musique. Ils ont en outre, accès à une dizaine d’ordinateurs qui font partie de l’EPN (Espace Public Numérique).
                        <br>
                        Le projet pédagogique, repensé tous les cinq ans, et le plan d’action, revu annuellement, de l’EdD visent à développer les capacités de l’enfant, sa créativité, son esprit critique et d’analyse, la gestion de son temps et son autonomie afin de lui permettre de tendre vers l’épanouissement. Notre action de terrain s’inspire directement des quatre missions inscrites au décret soit :
                        <br>
                        - le développement intellectuel de l'enfant, notamment par le soutien à sa scolarité, par l'aide aux devoirs;
                        <br>
                        - le développement et l'émancipation sociale de l'enfant, notamment par un suivi actif et personnalisé, dans le respect des différences, dans un esprit de solidarité et dans une approche interculturelle;
                        <br>
                        - la créativité de l'enfant, son accès et son initiation aux cultures dans leurs différentes dimensions, par des activités ludiques, d'animation, d'expression, de création et de communication;
                        <br>
                        - l'apprentissage de la citoyenneté et de la participation.
                        <br>
                        De plus, nous insistons pour créer du lien et un climat de collaboration étroite avec les parents afin de produire un angle d’approche supplémentaire et complémentaire qui encourage l’échange et le dialogue. Dans ce sens, les enfants de l’EdD, qui bénéficient de l’appui de l’informatique dans le soutien scolaire, ont un blog qui peut faire trait d’union avec les parents en leur offrant un regard sur les activités vécues au sein de l’EdD. Nous essayons de faire de l’EdD un terrain privilégié aux échanges de tout type tant interculturels qu’intergénérationnels.
                        <br><br>
                        L’équipe, qui est composée d’employé(e)s et de volontaires, repense régulièrement l’action et le travail auprès des enfants afin de s’ajuster au mieux aux réalités sociales, familiales et scolaires de notre public. Pour cela, ils participent à un travail en réseau et s’inscrivent dans des réflexions menées par différents groupes de travail ou lors de formations continues.
