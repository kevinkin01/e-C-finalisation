                <h2>Alphabétisation</h2>
                <div class="paraCulture">
                    <p><br>
                        <strong>Cours d’alphabétisation et de Français Langue Etrangère</strong> <br>
                        <br>
                        Notre pratique sociale de terrain nous permet d’identifier une série de situations problématiques liées à une méconnaissance du français, à une utilisation déficiente de la langue ou à une absence totale de scolarisation entraînant l’analphabétisme. Le public qui bénéficie de nos services se voit fréquemment confronté à de nombreuses situations d’isolement social, provoquées par le manque de communication, causant différents obstacles relationnels dans les rapports quotidiens avec les voisins, les commerçants, les administrations… Ces difficultés entraînent de nombreux problèmes psychologiques, sentiment d’humiliation, rejet de l’autre, repli communautaire et finalement absence d’intégration sociale.
                        <br><br>
                        Pour tenter de remédier à ces situations difficiles et quotidiennes, l’ASBL Entraide et Culture propose aux adultes (à partir de 18 ans toutefois) différents types de formations :
                        <br>
                        soit une formation en alphabétisation,
                        <br>
                        soit des cours de Français Langue Etrangère.
                        <br>
                        Les cours se déroulent dans les locaux de l’ASBL, en journée. Nous accueillons six groupes de niveaux différents par an, à raison de neuf heures hebdomadaire au rythme du calendrier scolaire (35 semaines/an excepté les congés scolaires). Une participation de 12€ par trimestre est demandée.
                        <br><br>
                        En moyenne par année, il y de 90 à 130 apprenants qui fréquentent nos cours.
                        <br><br>
                        La plupart de nos enseignants, employés ou volontaires, ont une formation de base pédagogique complétée par des formations régulières chez Lire et Écrire.
                        <br><br>
                        Plus d’informations :
                        <br>
                        Mme Véronique LEONARD au 02 539 43 76 ou alphafle@entraideetculture.be
                    </p>
